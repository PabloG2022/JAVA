public class Pessoa {

	public double altura;
	public double peso;
	
	public Pessoa() {
	}
	
	public Pessoa(double altura, double peso) {
		super();
		this.altura = altura;
		this.peso = peso;
	}
	public double getAltura() {
		return altura;
	}
	public void setAltura(double altura) {
		this.altura = altura;
	}
	public double getPeso() {
		return peso;
	}
	public void setPeso(double peso) {
		this.peso = peso;
	}

}

